
public class  Shape {
	int xStart;
	int yStart;
	int xEnd;
	int yEnd;
	String type;
	String shapeColor;
	int shape_iduser;
	public Shape(int xStart, int yStart, int xEnd, int yEnd, String type, String shapeColor, int shape_iduser) {
		super();
		this.xStart = xStart;
		this.yStart = yStart;
		this.xEnd = xEnd;
		this.yEnd = yEnd;
		this.type = type;
		this.shapeColor = shapeColor;
		this.shape_iduser = shape_iduser;
	}
	
	
	

}
